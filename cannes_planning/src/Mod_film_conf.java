    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
           String nomFilmSelec = "NomTest";
           String horaireSelec = "HoraireTest";
                   
           String msg = "Voulez vous placer "+nomFilmSelec+"\n à "+horaireSelec+" ?";
           
           int name = JOptionPane.showConfirmDialog(rootPane,msg, "Êtes-vous sûr ?",OK_CANCEL_OPTION,WARNING_MESSAGE);
           
           if (name == YES_OPTION){
               
                String salle = (String) jTable1.getValueAt(0,5);
                String creneaux = (String) jComboBox1.getSelectedItem();
               try {
                   controller.setCreneaux(creneaux,salle);
               } catch (ParseException ex) {
                   Logger.getLogger(Mod_film_conf.class.getName()).log(Level.SEVERE, null, ex);
               } catch (SQLException ex) {
                   Logger.getLogger(Mod_film_conf.class.getName()).log(Level.SEVERE, null, ex);
               }
               
                this.setVisible(false);
                Point x = this.getLocation();
                JFrame planning = null;
               try {
                   planning = new Afficher_planning_frame();
               } catch (SQLException ex) {
                   Logger.getLogger(Mod_film_conf.class.getName()).log(Level.SEVERE, null, ex);
               }
                planning.setLocation(x);
                planning.setVisible(true);
            }
    }//GEN-LAST:event_jButton2ActionPerformed

